import java.util.Scanner;
class while_
{
    public static void main(String[] args)
    {
        int n,i=1;
        Scanner obj=new Scanner(System.in);
        System.out.println("how many times u want to repeat:");
        n=obj.nextInt();
        while(i<=n)
        {
            System.out.println("ARNAB DAW");
            ++i;
        }
    }
}
