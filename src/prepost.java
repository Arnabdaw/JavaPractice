class prepost
{
    public static void main(String[] args)
    {
        int a=10;
        System.out.println(a);
        System.out.println("PREPOST INCREMENT");
        System.out.println(a++);
        System.out.println(++a);
        System.out.println("PREPOST DECREMENT");
        System.out.println(a--);
        System.out.println(--a);
    }
}
